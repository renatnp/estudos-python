# Variáveis armazenam valores na memória, que podem mudar durante o programa

nome = "João"          # string: texto
idade = 25             # inteiro
altura = 1.75          # float: número decimal
estudando = True       # booleano: True ou False

print(f"Nome: {nome}")
print(f"Idade: {idade}")
print(f"Altura: {altura}")
print(f"Estudando: {estudando}")